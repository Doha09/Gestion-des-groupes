    private void loadGrpage() {
        System.out.println("üîÑ Chargement des groupes...");

        GroupeList.removeAllItems(); // Vider la liste avant de la remplir

        GrpageService gs = new GrpageService(); // Cr√©er une instance du service

        for (Grpage g : gs.findAll()) {
            System.out.println("‚úÖ Ajout du groupe : " + g.getNomGroupe());
            GroupeList.addItem(g); // Ajouter le groupe dans la liste d√©roulante
        }

        if (GroupeList.getItemCount() > 0) {
            GroupeList.setSelectedIndex(0); // S√©lectionner le premier √©l√©ment par d√©faut
        }
    }

    public void load() {
        model.setRowCount(0);
        for (Etudiant e : es.findAll()) {
            model.addRow(new Object[]{e.getId(), e.getNom(), e.getPrenom(), e.getEmail(), e.getGroupe()});
        }

    }

