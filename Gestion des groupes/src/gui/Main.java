    private void bnConnexionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnConnexionActionPerformed

        String login = txtLogin.getText().trim();
        String password = new String(txtPassword.getPassword()).trim();

        System.out.println("üîç Tentative de connexion...");
        System.out.println("Login entr√© : " + login);
        System.out.println("Mot de passe entr√© : " + password);

        UtilisateurService utilisateurService = new UtilisateurService();
        Utilisateur utilisateurConnecte = utilisateurService.authenticate(login, password);

        if (utilisateurConnecte != null) {
            System.out.println("üîê Mot de passe hach√© en BD : " + utilisateurConnecte.getPassword());

            // V√©rifier la correspondance avec bcrypt
            if (BCrypt.checkpw(password, utilisateurConnecte.getPassword())) {
                System.out.println("‚úÖ Mot de passe correct !");
                JOptionPane.showMessageDialog(this, "Bienvenue, " + utilisateurConnecte.getLogin() + " !");
                MDIApplication mdi = new MDIApplication(utilisateurConnecte);
                mdi.setVisible(true);
                this.dispose();
            } else {
                System.out.println("‚ùå Mot de passe incorrect !");
                JOptionPane.showMessageDialog(this, "Login ou Mot de passe incorrect");
            }
        } else {
            System.out.println("‚ùå Utilisateur non trouv√© !");
            JOptionPane.showMessageDialog(this, "Login ou Mot de passe incorrect");

        }

    }//GEN-LAST:event_bnConnexionActionPerformed

