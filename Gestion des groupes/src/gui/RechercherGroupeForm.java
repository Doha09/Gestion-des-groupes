    private void rechercherGroupe() {
    String searchText = txtRechercheGroupe.getText().trim(); // Récupérer le texte entré par l'utilisateur
    List<Grpage> groupes = gs.findByNom(searchText); // Rechercher dans la BD

    model.setRowCount(0); // Effacer les anciennes données avant d'afficher les nouvelles

    for (Grpage g : groupes) {
        model.addRow(new Object[]{
            g.getId(), g.getNomGroupe(), g.getNiveau(), g.getFiliere(), g.getEffectif()
        });
    }
}

