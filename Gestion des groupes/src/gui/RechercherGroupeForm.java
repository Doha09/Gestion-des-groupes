    private void afficherTousGroupes() {
    List<Grpage> groupes = gs.findAll(); // Récupérer tous les groupes

    model.setRowCount(0); // Effacer les anciennes données
    
    for (Grpage g : groupes) {
        model.addRow(new Object[]{
            g.getId(), g.getNomGroupe(), g.getNiveau(), g.getFiliere(), g.getEffectif()
        });
    }
}

