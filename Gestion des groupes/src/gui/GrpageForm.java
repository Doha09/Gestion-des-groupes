    public void load() {
        model.setRowCount(0);
        for (Grpage g : gs.findAll()) {
            model.addRow(new Object[]{g.getId(), g.getNomGroupe(), g.getNiveau(), g.getFiliere(), g.getEffectif()});
        }
    }

