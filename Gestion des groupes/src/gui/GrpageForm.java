    private void bnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnAddActionPerformed
        String nomgroupe = txtNomGroupe.getText().toString();
        String niveau = txtNiveau.getText().toString();
        String filiere = txtFiliere.getText().toString();
        int effectif = Integer.parseInt(txtEffectif.getText().trim());
        if (gs.create(new Grpage(nomgroupe, niveau, filiere, effectif))) {
            JOptionPane.showMessageDialog(this, "Bien enregistrée");
            load();
        } else {
            JOptionPane.showConfirmDialog(this, "Problème");

        }
    }//GEN-LAST:event_bnAddActionPerformed

    private void bnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnDeleteActionPerformed
        int response = JOptionPane.showConfirmDialog(this, "Voulez vous vraiment supprimer ce groupe?");
        if (response == 0) {
            gs.delete(gs.findById(id));
            load();
        }
    }//GEN-LAST:event_bnDeleteActionPerformed

    private void ListeGrpageMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ListeGrpageMouseClicked
        id = Integer.parseInt(model.getValueAt(ListeGrpage.getSelectedRow(), 0).toString());
        txtNomGroupe.setText(model.getValueAt(ListeGrpage.getSelectedRow(), 1).toString());
        txtNiveau.setText(model.getValueAt(ListeGrpage.getSelectedRow(), 2).toString());
        txtFiliere.setText(model.getValueAt(ListeGrpage.getSelectedRow(), 3).toString());
        txtEffectif.setText(model.getValueAt(ListeGrpage.getSelectedRow(), 4).toString());
    }//GEN-LAST:event_ListeGrpageMouseClicked

    private void bnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnUpdateActionPerformed
        int response = JOptionPane.showConfirmDialog(this, "Voulez vous vraiment modifier ce groupe?");
        if (response == 0) {

            String nomgroupe = txtNomGroupe.getText().toString();
            String niveau = txtNiveau.getText().toString();
            String filiere = txtFiliere.getText().toString();
            int effectif = Integer.parseInt(txtEffectif.getText().trim());
            if (gs.update(new Grpage(id, nomgroupe, niveau, filiere, effectif))) {
                JOptionPane.showMessageDialog(this, "Bien enregistrée");
                load();
            } else {
                JOptionPane.showConfirmDialog(this, "Problème");

            }
        }
    }//GEN-LAST:event_bnUpdateActionPerformed


