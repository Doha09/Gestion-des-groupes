    private void bnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnAddActionPerformed

        String intitule = txtIntitule.getText().toString();
        String professeur = txtProfesseur.getText().toString();
        Grpage groupe = (Grpage) GroupeList.getSelectedItem();
        if (cs.create(new Cours(intitule, professeur, groupe))) {
            JOptionPane.showMessageDialog(this, "Bien enregistré");
            load();
        } else {
            JOptionPane.showConfirmDialog(this, "Problème");
        }

    }//GEN-LAST:event_bnAddActionPerformed

    private void listeCoursMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listeCoursMouseClicked
        id = Integer.parseInt(model.getValueAt(listeCours.getSelectedRow(), 0).toString());
        txtIntitule.setText(model.getValueAt(listeCours.getSelectedRow(), 1).toString());
        txtProfesseur.setText(model.getValueAt(listeCours.getSelectedRow(), 2).toString());
        Grpage grpage = (Grpage) GroupeList.getSelectedItem();

    }//GEN-LAST:event_listeCoursMouseClicked

    private void bnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnDeleteActionPerformed
        int response = JOptionPane.showConfirmDialog(this, "Voulez vous vraiment supprimer ce cours ?");
        if (response == 0) {
            cs.delete(cs.findById(id));
            load();
        }
    }//GEN-LAST:event_bnDeleteActionPerformed

    private void bnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnUpdateActionPerformed
        int response = JOptionPane.showConfirmDialog(this, "Voulez vous vraiment modifier ce cours ?");
        if (response == 0) {

            String intitule = txtIntitule.getText().toString();
            String professeur = txtProfesseur.getText().toString();
            Grpage groupe = (Grpage) GroupeList.getSelectedItem();
            if (cs.update(new Cours(id, intitule, professeur, groupe))) {
                JOptionPane.showMessageDialog(this, "Bien enregistré");
                load();
            } else {
                JOptionPane.showConfirmDialog(this, "Problème");
            }
        }
    }//GEN-LAST:event_bnUpdateActionPerformed

