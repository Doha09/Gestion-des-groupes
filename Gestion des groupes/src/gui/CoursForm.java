    private void loadGrpage() {
        GroupeList.removeAllItems();
        for (Grpage g : gs.findAll()) {
            GroupeList.addItem(g);
        }
    }

    public void load() {
        model.setRowCount(0);
        for (Cours c : cs.findAll()) {
            model.addRow(new Object[]{c.getId(), c.getIntitule(), c.getProfesseur(), c.getGroupe().getNomGroupe()});
        }
    }

